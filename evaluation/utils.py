def compute_pass_at_k(n, c, k):
    if n - c < k:
        return float(100)
    return float(f"{(1.0 - np.prod(1.0 - k / np.arange(n - c + 1, n + 1)))*100:.2f}")


def extract_terminal_pairs(obj, parent_key=''):
    result = []
    if isinstance(obj, dict):
        for k, v in obj.items():
            new_key = f"{parent_key} {k}".strip()
            if isinstance(v, (dict, list)):
                result.extend(extract_terminal_pairs(v, new_key))
            else:
                result.append(f"{new_key} {v}")
    elif isinstance(obj, list):
        for item in obj:
            if isinstance(item, (dict, list)):
                result.extend(extract_terminal_pairs(item, parent_key))
            else:
                result.append(f"{parent_key} {item}".strip())
    return result

def calculate_sentence_selfBleu(sentence, remaining_sentences):
    lst = []
    for i in remaining_sentences:
        smoothing = SmoothingFunction().method1
        bleu = sentence_bleu([sentence], i, smoothing_function=smoothing)
        lst.append(bleu)
    return lst


def calculate_selfBleu(sentences):
    '''
    sentences - list of sentences generated by NLG system
    '''
    bleu_scores = []

    for i in sentences:
        sentences_copy = copy.deepcopy(sentences)
        remaining_sentences = sentences_copy.remove(i)
        bleu = calculate_sentence_selfBleu(i,sentences_copy)
        bleu_scores.append(bleu)
    if math.isnan(mean(bleu_scores)):
        return 0
    return mean(bleu_scores)

def compute_contradiction_label(ground_truth: str, inference: str, nli_model) -> float:

    scores = nli_model.predict([ground_truth, inference], apply_softmax=True)
    label = ['contradiction', 'entailment', 'neutral'][scores.argmax()]
    return {
        'label': label,
        'contradiction': scores[0],
        'entailment': scores[1],
        'neutral': scores[2],
    }

def compute_contradiction_score(candidate: str, reference: str):
    nli_model = CrossEncoder('cross-encoder/nli-deberta-v3-base')
    sentences = extract_terminal_pairs(candidate)
    contradiction_count = 0
    for sentence in sentences:
        if compute_contradiction_label(reference, sentence,nli_model)['label'] == 'contradiction':
            contradiction_count = contradiction_count+1
    return contradiction_count/len(sentences)


def compute_bleu(candidate: str, reference: str) -> float:
    """
    Computes the BLEU score between a candidate and a reference string.

    Parameters:
    - candidate (str): The generated text to evaluate.
    - reference (str): The reference text to compare against.

    Returns:
    - float: The BLEU score.
    """
    # Tokenize the input strings
    candidate_tokens = candidate.split()
    reference_tokens = reference.split()

    # Apply smoothing to handle cases with no matching n-grams
    smoothing = SmoothingFunction().method1

    # Compute the BLEU score
    score = sentence_bleu([reference_tokens], candidate_tokens, smoothing_function=smoothing)
    return score

def analyze_json_structure(json_obj):
    max_depth = 0
    total_keys = 0
    keys_per_depth = {}
    total_value_length = 0
    total_values = 0

    def traverse(obj, depth):
        nonlocal max_depth, total_keys, total_value_length, total_values
        if isinstance(obj, dict):
            max_depth = max(max_depth, depth)
            keys_per_depth[depth] = keys_per_depth.get(depth, 0) + len(obj)
            total_keys += len(obj)
            for value in obj.values():
                traverse(value, depth + 1)
        elif isinstance(obj, list):
            for item in obj:
                traverse(item, depth)
        elif isinstance(obj, str):
            total_value_length += len(obj)
            total_values += 1

    traverse(json_obj, 1)

    average_value_length = total_value_length / total_values if total_values else 0

    return {
        "max_depth": max_depth,
        "total_keys": total_keys,
        "keys_per_depth": keys_per_depth,
        "average_value_length": average_value_length
    }

